// Set up provider and signer
const provider = new ethers.providers.Web3Provider(window.ethereum);
const signer = provider.getSigner();

// Set up contract instance


// Function to stake tokens
export const stake = async (amount,address) => {
  const contractAddress = address
  const timelockContract = new ethers.Contract(contractAddress, timelockABI, signer);
  const tx = await timelockContract.stake(amount);
  await tx.wait();
  alert("Staked", amount, "tokens");
}

// Function to unstake tokens from a specific pool index
export const unstake =async (poolIndex,address) => {
  const contractAddress = address
  const timelockContract = new ethers.Contract(contractAddress, timelockABI, signer);
  const tx = await timelockContract.unstake(poolIndex);
  await tx.wait();
  alert("Unstaked tokens from pool", poolIndex);
}

// Function to get rewards for a specific pool index
export const getRewards =async (poolIndex,address) => {
  const contractAddress = address
  const timelockContract = new ethers.Contract(contractAddress, timelockABI, signer);
  const reward = await timelockContract.gettotalreward(signer.getAddress(), poolIndex);
  alert("Rewards for pool", poolIndex, ":", reward.toString());
}